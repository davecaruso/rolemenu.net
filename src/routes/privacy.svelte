<h1>Privacy Policy</h1>

<h2>Data</h2>
<p>This application accesses the following from Discord:</p>
<ul>
	<li>Username and Tag.</li>
	<li>User Profile Picture.</li>
	<li>List of servers you are in, and their icons.</li>
	<li>List of roles for all servers the bot is in.</li>
	<li>List of emojis for all servers the bot is in.</li>
</ul>
<p>The following is stored:</p>
<ul>
	<li>Server, Channel, and Message ID where your Role Menus are located</li>
	<li>Role Menu information such as the message content, role labels, emojis, etc.</li>
	<li>When each role menu was last modified.</li>
	<li>When each role menu was last interacted with.</li>
	<li>Global statistics of how often menus are created, interacted with, edited, and deleted.</li>
	<li>Errors on the website, bot, and backend</li>
</ul>

<p>
	Telemetry data is collected to help improve the application and monitor usage and growth. The rest
	of the data should be obviously required for the application to function.
</p>

<h2>Tracking</h2>
<p>
	Cloudflare Web Analytics is used to track how many people use the website and bot. I also look at
	the total count of role menus in the database, and usage frequency.
</p>

<h2>Deleting Data</h2>
<p>
	To delete your personal data, remove the discord message for your role menus or through the
	website, and log out. The stored data may take up to 7 days to be purged from the system, it will
	instantly be inaccessible to all users.
</p>
<p>Global statistics are not deleted, as they cannot be tracked down to the specific user.</p>
